# Задача-1:
# Дан список фруктов.
# Напишите программу, выводящую фрукты в виде нумерованного списка,
# выровненного по правой стороне.

# Пример:
# Дано: ["яблоко", "банан", "киви", "арбуз"]
# Вывод:
# 1. яблоко
# 2.  банан
# 3.   киви
# 4.  арбуз

# Подсказка: воспользоваться методом .format()

print('Курс: Python1; урок: 2; уровень: easy; задача: 1')
fruits = ['яблоко', 'банан', 'киви', 'арбуз', 'манго', 'помело', 'апельсин']
max_len = 0

for fruit in fruits: #определяем максимальную длину элемента списка для сдвига
    if len(fruit) > max_len:
        max_len = len(fruit)

i = 1 # вывод списка в соответствии с форматом задания
for fruit in fruits:
    print(i,  '{:>{length}}'.format(fruit, length = max_len), sep='. ')
    i += 1

# Задача-2:
# Даны два произвольные списка.
# Удалите из первого списка элементы, присутствующие во втором списке.

print('\n'+'Курс: Python1; урок: 2; уровень: easy; задача: 2')
