# Задача-3:
# Дан произвольный список из целых чисел.
# Получите НОВЫЙ список из элементов исходного, выполнив следующие условия:
# если элемент кратен двум, то разделить его на 4, если не кратен, то умножить на два.

print('\n'+'Курс: Python1; урок: 2; уровень: easy; задача: 3')
list3 = [69, 2, 53, 44, 62, 70, 19, 48, 83, 5, 64, 6, 29, 43, 59, 73, 19, 61, 84, 81, 83, 54, 5, 22, 51, 41, 42, 96, 7, 34]
print('Исходный список: ', list3)
list4 = []

for elem in list3:
    if elem % 2 == 0:
        list4.append(elem/4)
    else:
        list4.append(elem*2)
print('Новый список:',list4)